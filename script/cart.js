function updateCart(input) {
    // Access the form containing the input element
    var form = input.form;

    // Show the update button in the current form
    form.querySelector('input[type="submit"]').style.display = 'block';
}

document.addEventListener("DOMContentLoaded", function () {
    // Create an Intersection Observer
    var observer = new IntersectionObserver(function (entries) {
        entries.forEach(function (entry) {
            if (entry.isIntersecting) {
                entry.target.classList.add("show");
            }
        });
    }, { threshold: 0.5 });

    // Observe each featured element
    document.querySelectorAll('.each-cart-product').forEach(function (element) {
        observer.observe(element);
    });
});

